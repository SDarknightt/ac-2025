    String[][] convertStringToMatrix(String[] input) {
        // Quantas posições na vertical e horizontal
        String[][] matrix = new String[input.length][input[0].length()];

        //Itera verticalmente
        for (int i = 0; i < input.length; i++) {
            String[] current = input[i].split("");
            // Itera horizontalmente
            for (int j = 0; j < input[i].length(); j++) {
                matrix[i][j] = current[j];
            }
        }
        return matrix;
    }

    int verifyAxysX(String[][] matrix, int X, int Y) {
        int count = 0;
        // Verifica se esta nas bordas

        // Verifica esquerda
        if (Y > 0) {
            if (matrix[X][Y-1].equals("@")) {
                count++;
            }
        }

        // Verifica a direita
        if (Y < (matrix[0].length-1)) {
            if (matrix[X][Y+1].equals("@")) {
                count++;
            }
        }

        return count;
    }

    int verifyAxysY(String[][] matrix, int X, int Y) {
        int count = 0;
        // Verifica se esta nas bordas

        // Verifica cima
        if (X > 0) {
            if (matrix[X-1][Y].equals("@")) {
                count++;
            }
        }

        // Verifica a abaixo
        if (X < (matrix.length-1)) {
            if (matrix[X+1][Y].equals("@")) {
                count++;
            }
        }

        return count;
    }

    int verifyAxysDiagonalLeftToRight(String[][] matrix, int X, int Y) {
        int count = 0;
        // Verifica se esta nas bordas

        // Verifica cima esquerda
        if (X > 0 && Y > 0) {
            if (matrix[X-1][Y-1].equals("@")) {
                count++;
            }
        }

        // Verifica baixo direita
        if (X < (matrix.length-1) && Y < (matrix[0].length-1)) {
            if (matrix[X+1][Y+1].equals("@")) {
                count++;
            }
        }

        return count;
    }

    int verifyAxysDiagonalRightToLeft(String[][] matrix, int X, int Y) {
        int count = 0;

        // Verifica cima direita
        if (X > 0 && Y < (matrix[0].length-1)) {
            if (matrix[X-1][Y+1].equals("@")) {
                count++;
            }
        }

        // Verifica baixo esquerda
        if (X < (matrix.length-1) && Y > 0) {
            if (matrix[X+1][Y-1].equals("@")) {
                count++;
            }
        }

        return count;
    }

    void rollsCounter(String[][] matrix) {
        // Itera verticalmente
        for (int i = 0; i < matrix.length; i++) {
            // Itera horizontalmente
            for (int j = 0; j < matrix[0].length; j++) {

                int rollArround = 0;
                // Horizontal
                rollArround += verifyAxysX(matrix, i, j);
                // Vertical
                rollArround += verifyAxysY(matrix, i, j);
                // Diagonal esquerda para direita
                rollArround += verifyAxysDiagonalLeftToRight(matrix, i, j);
                // Diagonal direita para esquerda
                rollArround += verifyAxysDiagonalRightToLeft(matrix, i, j);

                if (matrix[i][j].equals("@")) {
                    if (rollArround < 4) {
                        this.totalRollsRemoved++;
                    }
                }
            }
        }
    }

    void printResult() {
        System.out.println("Total rolls removed: " + this.totalRollsRemoved);
    }
